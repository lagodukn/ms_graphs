# Função para encontrar os 5 estados com maior delta de crescimento ou decaída de uma vacina específica
def encontrar_delta(dados_vacina, doenca, vacinas):
    percentuais = defaultdict(lambda: defaultdict(dict))

    for ano, estados in dados_vacina.items():
        for estado, doencas in estados.items():
            if doenca in doencas:
                vacinas_dado = doencas[doenca]
                for vacina in vacinas:
                    if vacina in vacinas_dado:
                        percentual_atual = vacinas_dado[vacina]
                        if percentual_atual is not None:
                            percentuais[vacina][estado][ano] = percentual_atual

    deltas = defaultdict(list)
    for vacina, estados_percentuais in percentuais.items():
        for estado, anos_percentuais in estados_percentuais.items():
            anos_sorted = sorted(anos_percentuais.items())
            for i in range(1, len(anos_sorted)):
                ano_anterior, percentual_anterior = anos_sorted[i - 1]
                ano_atual, percentual_atual = anos_sorted[i]
                if percentual_anterior is not None and percentual_atual is not None:
                    delta = percentual_atual - percentual_anterior
                    tipo_delta = "Crescimento" if delta > 0 else "Decaída"
                    deltas[vacina].append((estado, ano_anterior, ano_atual, tipo_delta, delta))

    # Plotar gráfico para deltas
    for vacina, deltas_list in deltas.items():
        deltas_sorted = sorted(deltas_list, key=lambda x: abs(x[4]), reverse=True)

        estados_deltas = [estado for estado, _, _, _, _ in deltas_sorted[:5]]
        anos_deltas = [f'{ano_anterior} - {ano_atual}' for _, ano_anterior, ano_atual, _, _ in deltas_sorted[:5]]
        tipos_deltas = [tipo_delta for _, _, _, tipo_delta, _ in deltas_sorted[:5]]
        valores_deltas = [delta for _, _, _, _, delta in deltas_sorted[:5]]
        nome_arquivo = f'{doenca}_{vacina}_deltas.png'
        salvar_grafico(f'Top 5 Estados com Maior Delta para Vacina {vacina} em {doenca}', 
                       estados_deltas, 
                       valores_deltas, 
                       [f'{ano_delta}\n{tipo_delta}' for ano_delta, tipo_delta in zip(anos_deltas, tipos_deltas)], 
                       nome_arquivo)

# Função para encontrar os 5 piores estados em relação à diferença entre duas vacinas
def encontrar_piores_diferencas(dados_vacina, doenca, vacina1, vacina2):
    diferencas = []
    
    for ano, estados in dados_vacina.items():
        for estado, doencas in estados.items():
            if doenca in doencas:
                vacinas_dado = doencas[doenca]
                percentual1 = vacinas_dado.get(vacina1)
                percentual2 = vacinas_dado.get(vacina2)
                if percentual1 is not None and percentual2 is not None:
                    diferenca = percentual1 - percentual2
                    diferencas.append((estado, ano, diferenca))
    
    piores_diferencas = heapq.nsmallest(5, diferencas, key=lambda x: x[2])
    
    # Plotar gráfico para piores diferenças
    estados_piores = [estado for estado, _, _ in piores_diferencas]
    anos_piores = [ano for _, ano, _ in piores_diferencas]
    diferencas_piores = [diferenca for _, _, diferenca in piores_diferencas]
    nome_arquivo = f'{doenca}_{vacina1}_{vacina2}_piores_diferencas.png'
    salvar_grafico(f'Top 5 Piores Diferenças entre Vacinas {vacina1} e {vacina2} em {doenca}', 
                   [f'{estado} ({ano})' for estado, ano in zip(estados_piores, anos_piores)], 
                   diferencas_piores, 
                   [f'{estado} ({ano})' for estado, ano in zip(estados_piores, anos_piores)], 
                   nome_arquivo)

